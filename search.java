    private void btnsearch1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearch1ActionPerformed
        // TODO add your handling code here:
                                                         
    try {
        int guideId = Integer.parseInt(txtsearchid2.getText().trim());
        Guide_Database guideDatabase = new Guide_Database("", 0, "");
        ResultSet rs = guideDatabase.searchGuideById(guideId);

        DefaultTableModel model = (DefaultTableModel) display.getModel();
        model.setRowCount(0); 

        boolean idFound = false;

        while (rs.next()) {
            idFound = true;
            int id = rs.getInt("id");
            String name = rs.getString("name");
            String language = rs.getString("language");
            model.addRow(new Object[]{id, name, language});
        }

        rs.close();

        if (idFound) {
            JOptionPane.showMessageDialog(null, "ID found successfully.");
            txtsearchid2.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "ID not found.");
             txtsearchid2.setText("");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error while searching for guide: " + e.getMessage());
         txtsearchid2.setText("");
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Please enter a valid guide ID.");
         txtsearchid2.setText("");
    }


        
    }//GEN-LAST:event_btnsearch1ActionPerformed

