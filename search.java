    private void btnsearch2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearch2ActionPerformed
        // TODO add your handling code here:
                                                         
    String searchIdStr = txtsearchid3.getText().trim();

    if (searchIdStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a regular visitor ID to search.", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int searchId;
    try {
        searchId = Integer.parseInt(searchIdStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid regular visitor ID. Please enter a numeric value.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    RegularVisitor_Database rvDatabase = new RegularVisitor_Database("", 0, "");
    ResultSet rs = rvDatabase.searchVisitorById(searchId);

    if (rs == null) {
        JOptionPane.showMessageDialog(this, "Regular visitor not found or failed to fetch data.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Declare SimpleDateFormat objects
    SimpleDateFormat dbFormat = new SimpleDateFormat("yyyy-MM-dd");
    SimpleDateFormat displayFormat = new SimpleDateFormat("dd-MM-yyyy");

    try {
        if (rs.next()) {
            int id = rs.getInt("ID");
            String name = rs.getString("Name");
            String dobStr = rs.getString("Date of birth");
            
            try {
                // Parse the date from the database format
                Date dob = dbFormat.parse(dobStr);
                
                // Format the date to display format
                String formattedDob = displayFormat.format(dob);
                
                // Display the information in the respective text fields
                txtsearchid3.setText(String.valueOf(id));
           
                // Update the table model with the search result
                DefaultTableModel model = (DefaultTableModel) display.getModel();
                model.setRowCount(0); // Clear existing data
                model.addRow(new Object[]{id, name, formattedDob,"-"});
            } catch (ParseException e) {
                // Handle the parsing exception
                System.err.println("Error parsing date: " + e.getMessage());
                JOptionPane.showMessageDialog(this, "Error parsing date: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Regular visitor not found.", "Information", JOptionPane.INFORMATION_MESSAGE);
            txtsearchid3.setText("");
            
            // Clear the table if no data is found
            DefaultTableModel model = (DefaultTableModel) display.getModel();
            model.setRowCount(0);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error while displaying regular visitor: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnsearch2ActionPerformed

