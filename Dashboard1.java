        // TODO add your handling code here:
       
               DefaultTableModel model = (DefaultTableModel) table1.getModel();
                 model.setRowCount(0);


        RegularVisitor_Database rvisitor = new RegularVisitor_Database("", 0, "");
        ResultSet rs = rvisitor.fetchVisitors();
        if(rs==null)
        {
            JOptionPane.showMessageDialog(null, "failed to fetch data");
        }
        
            SimpleDateFormat dbFormat = new SimpleDateFormat("yyyy-MM-dd");
            SimpleDateFormat displayFormat = new SimpleDateFormat("dd-MM-yyyy");
        try {
            
                while (rs.next()) {
                    
                    String name = rs.getString("Name");
                    int id = rs.getInt("ID");
                    String dateOfBirth = rs.getString("Date of Birth");
                    String formattedDate = null;
                    try
                    {
                        Date dob = dbFormat.parse(dateOfBirth);
                        formattedDate=displayFormat.format(dob);
                    }
                    catch(ParseException e)
                            {
                              System.err.println("Error parsing date "+e.getMessage());
                            }
                    
                    model.addRow(new Object[]{name, id, formattedDate,"-"});
                    
                }
                rs.close();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error fetching data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        table1.setModel(model);

