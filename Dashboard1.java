    private void btndisplay1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndisplay1ActionPerformed
        // TODO add your handling code here:
                DefaultTableModel tableModel = (DefaultTableModel) table1.getModel();
        tableModel.setRowCount(0); 

        // Fetch data from database
        Guide_Database guideDatabase = new Guide_Database("", 0, ""); 
        ResultSet rs = guideDatabase.fetchGuides();
        if (rs == null) {
            JOptionPane.showMessageDialog(this, "Failed to fetch data from the database.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            boolean hasData = false;
            while (rs.next()) {
                hasData = true;
                int id = rs.getInt("id");
                String name = rs.getString("name");
                String language = rs.getString("language");
                tableModel.addRow(new Object[]{id, name,"-", language});
            }
            if (!hasData) {
                JOptionPane.showMessageDialog(this, "The Guide table is empty.", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error while displaying guides: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

       
    }//GEN-LAST:event_btndisplay1ActionPerformed

    private void btndisplay3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndisplay3ActionPerformed
                                        
               DefaultTableModel tableModel = (DefaultTableModel) table1.getModel();
    tableModel.setRowCount(0);

    // Fetch data from database
    SeniorVisitor_Database svisitor = new SeniorVisitor_Database("", 0, ""); 
    ResultSet rs = svisitor.fetchVisitors();
    if (rs == null) {
        JOptionPane.showMessageDialog(this, "Failed to fetch data from the database.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Declare SimpleDateFormat objects
    SimpleDateFormat dbFormat = new SimpleDateFormat("yyyy-MM-dd");
    SimpleDateFormat displayFormat = new SimpleDateFormat("dd-MM-yyyy");

    try {
        boolean hasData = false;
        while (rs.next()) {
            hasData = true;
            int id = rs.getInt("ID");
            String name = rs.getString("Name");
            String dobStr = rs.getString("Date of Birth");

            String formattedDob;
            try {
                // Parse the date from the database format
                Date dob = dbFormat.parse(dobStr);
                // Format the date to display format
                formattedDob = displayFormat.format(dob);
            } catch (ParseException e) {
                // Handle the parsing exception
                System.err.println("Error parsing date: " + e.getMessage());
                formattedDob = dobStr; // Use the original date string in case of error
            }

            tableModel.addRow(new Object[]{id, name, formattedDob,"-"});
        }
        if (!hasData) {
            JOptionPane.showMessageDialog(this, "The senior visitor table is empty.", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error while displaying senior visitors: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btndisplay3ActionPerformed

